# menghitung bilangan berpangkat 2

def pangkat_dua(n):  
        return n**2  

# Meminta input dari pengguna
bilangan = int(input("Masukkan bilangan bulat: "))
hasil = pangkat_dua(bilangan) # memanggil fungsi pangkat_dua dan variabel bilangan
print(f"hasil dari {bilangan}^2 = {hasil}")








