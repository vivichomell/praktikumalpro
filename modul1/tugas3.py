# Masukkan Suhu Dalam Bentuk Celsius
celsius = float(input("Masukkan Suhu Celsius :"))
print()

# Rumus Konversi
farenheit = (9/5 * celsius) + 32
print(celsius, 'derajat celsius =',farenheit, 'derajat farenheit')

kelvin = celsius + 273.15
print(celsius, 'derajat celsius =',kelvin, 'derajat kelvin')

reamur = celsius * (4/5)
print(celsius, 'derajat celsius =',reamur, 'derajat reamur')